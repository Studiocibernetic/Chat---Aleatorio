  /// Custom message bubble colors for different states
  static Color getMessageBubbleColor(bool isSent, bool isLight) {
    if (isSent) {
      return isLight ? primaryLight : primaryDark;
    } else {
      return isLight ? messageReceivedLight : messageReceivedDark;
    }
  }

  /// Privacy state indicator colors
  static Color getPrivacyStateColor(String state, bool isLight) {
    switch (state) {
      case 'anonymous':
        return isLight ? accentLight : accentDark;
      case 'connected':
        return isLight ? secondaryLight : secondaryDark;
      case 'blocked':
        return isLight ? warningLight : warningDark;
      default:
        return isLight ? textSecondaryLight : textSecondaryDark;
    }
  }

  /// Connection status colors for user indicators
  static Color getConnectionStatusColor(bool isOnline, bool isLight) {
    return isOnline
        ? (isLight ? secondaryLight : secondaryDark)
        : (isLight ? textSecondaryLight : textSecondaryDark);
  }
